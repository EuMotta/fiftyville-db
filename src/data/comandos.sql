-- INTERVIEWS
SELECT
  *
FROM
  interviews
WHERE
  YEAR = 2021
  AND DAY = 28
  AND MONTH = 7
  AND transcript LIKE '%bakery%';

-- BAKERY_SECURITY_LOGS

SELECT
  BAKERY_SECURITY_LOGS.ACTIVITY,
  BAKERY_SECURITY_LOGS.LICENSE_PLATE,
  PEOPLE.NAME
FROM
  PEOPLE
  JOIN BAKERY_SECURITY_LOGS ON BAKERY_SECURITY_LOGS.LICENSE_PLATE = PEOPLE.LICENSE_PLATE
WHERE
  BAKERY_SECURITY_LOGS.DAY = 28
  AND BAKERY_SECURITY_LOGS.MONTH = 7
  AND BAKERY_SECURITY_LOGS.YEAR = 2021
  AND BAKERY_SECURITY_LOGS.HOUR = 10
  AND BAKERY_SECURITY_LOGS.MINUTE > 15
  AND BAKERY_SECURITY_LOGS.MINUTE < 30;

-- TRANSACTIONS
SELECT
  PEOPLE.NAME, PEOPLE.PHONE_NUMBER,
  ATM_TRANSACTIONS.TRANSACTION_TYPE
FROM
  PEOPLE
  JOIN ATM_TRANSACTIONS ON ATM_TRANSACTIONS.ACCOUNT_NUMBER = BANK_ACCOUNTS.ACCOUNT_NUMBER
  JOIN BANK_ACCOUNTS ON BANK_ACCOUNTS.PERSON_ID = PEOPLE.ID
WHERE
  ATM_LOCATION = 'Leggett Street'
  AND ATM_TRANSACTIONS.DAY = 28
  AND ATM_TRANSACTIONS.MONTH = 7
  AND ATM_TRANSACTIONS.YEAR = 2021;

-- PHONE CALLS
SELECT
  *
FROM
  phone_calls
WHERE
  phone_calls.DAY = 28
  AND phone_calls.MONTH = 7
  AND phone_calls.YEAR = 2021
  AND DURATION < 60;



